package day03practice;

public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("I love Java");
        System.out.println("My name: \nBüşra");
        System.out.println();
        System.out.println("What is your name");
        System.out.println("\tMy name is:Büşra");
        System.out.println();
        System.out.println("/  \\");
        System.out.println();
        System.out.println("My name is: \"Büşra");


    }
}
