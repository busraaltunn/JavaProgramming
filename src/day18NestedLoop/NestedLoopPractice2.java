package day18NestedLoop;

public class NestedLoopPractice2 {
    public static void main(String[] args) {

        for (int i = 0; i <10; i++) {

            for (int j = 0; j <10; j++) {  //i=0,2,3,4,5,6,7,8,9 (10 repeat)
                System.out.print("*  ");
            }

            System.out.println();


        }





    }
}
